	void print_info(uint16_t *buf, uint16_t len )
	{
		printf("{\r\n");
		for (uint8_t i = 0; i <=len; i++) {
			printf("%4d,", buf[i]);
			if (!(i%10)) {
				printf("\r\n");
			}
		}
		printf("};\r\n");
	}

